my_str = '0123456789'

# 使用<container[index]>调用容器中的元素
print(my_str[1])  # 1 索引从0开始
print(my_str[2:5])  # 234 [起始索引:终止索引] [起始索引:终止索引)左闭右开
print(my_str[2:5:1])  # 234 [起始索引:终止索引:步长] 步长不能为0，默认为1
print(my_str[:5:2])  # 024 起始索引留空默认为0
print(my_str[2::2])  # 2468 终止索引留空默认为最大索引
print(my_str[-6:-1])  # 45678 负数表示索引从末尾向左数
print(my_str[-1:-5])  # 无结果 终止索引应在起始索引正方向
print(my_str[-1:-5:-1])  # 9876 正方向由步长符号决定