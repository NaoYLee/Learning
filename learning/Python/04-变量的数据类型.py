# 整型
a = 10
print(a)
# 浮点型
b = 10.3
print(b)
# 字符串型
c = 'Monty Python'
print(c)
# Boolean型，值只有: True 和 False，也可以用0表示false，非0表示True
d = True  # Python 大小写敏感，Boolean类型必须首字母大写
print(d)

# Python会自动识别变量类型
print(type(a))  # int
print(type(b))  # float
print(type(c))  # str
print(type(d))  # bool
print(type('123'))  # str